/**
 * local-dev-setup.mjs
 *
 * Starts a local dev environment without Docker:
 *  1. Embedded PostgreSQL on port 5777
 *  2. Anvil fork of Base mainnet on port 8545
 *
 * Usage:
 *   node scripts/local-dev-setup.mjs
 *
 * Then in another terminal:
 *   npm run start:dev
 *
 * Press Ctrl+C to stop everything.
 */

import EmbeddedPostgres from 'embedded-postgres'
import { execSync, spawn } from 'child_process'
import path from 'path'
import { fileURLToPath } from 'url'
import fs from 'fs'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const rootDir = path.join(__dirname, '..')

// ── Config ────────────────────────────────────────────────────────────────────
const PG_PORT = 5777
const PG_USER = 'scl_user'
const PG_PASSWORD = 'scl_password'
const PG_DATABASE = 'scl_game'
const PG_DATA_DIR = '/tmp/scl-local-pg'

const ANVIL_PORT = 8545
const BASE_MAINNET_RPC = 'https://mainnet.base.org'
const CHECKIN_CONTRACT = '0x2eeE0Cf01AD4f67E41D487525E6068a8F732722F'

// ── Embedded Postgres ─────────────────────────────────────────────────────────
console.log('\n🐘 Starting embedded PostgreSQL...')

// Clean up old data dir if it exists (fresh start)
if (fs.existsSync(PG_DATA_DIR)) {
  fs.rmSync(PG_DATA_DIR, { recursive: true, force: true })
  console.log('   Cleaned up previous PG data dir')
}

const pg = new EmbeddedPostgres({
  databaseDir: PG_DATA_DIR,
  user: PG_USER,
  password: PG_PASSWORD,
  port: PG_PORT,
  database: PG_DATABASE,
  persistent: false,
})

try {
  await pg.initialise()
  await pg.start()
  await pg.createDatabase(PG_DATABASE)
  console.log(`✅ PostgreSQL running on port ${PG_PORT} (db: ${PG_DATABASE})`)
} catch (e) {
  console.error('❌ Failed to start embedded PostgreSQL:', e.message)
  process.exit(1)
}

// ── Write .env.local ──────────────────────────────────────────────────────────
const envLocal = `# AUTO-GENERATED by scripts/local-dev-setup.mjs — do not commit
DATABASE_URL=postgresql://${PG_USER}:${PG_PASSWORD}@localhost:${PG_PORT}/${PG_DATABASE}?schema=public
REDIS_DISABLED=true
PORT=3333
NODE_ENV=development
FRONTEND_URL=http://localhost:5173
GAME_LAUNCH_DATE=2026-02-06
WORDPRESS_URL=http://localhost:8080

# Blockchain monitor — local Anvil fork of Base mainnet
BLOCKCHAIN_RPC_URL=http://localhost:${ANVIL_PORT}
BLOCKCHAIN_NETWORK=base
SCL_DAILY_CHECKIN_ADDRESS=${CHECKIN_CONTRACT}
`
fs.writeFileSync(path.join(rootDir, '.env.local'), envLocal)
console.log('✅ .env.local written')

// ── Run Prisma migrations ─────────────────────────────────────────────────────
console.log('\n📦 Running Prisma migrations...')
try {
  const dbUrl = `postgresql://${PG_USER}:${PG_PASSWORD}@localhost:${PG_PORT}/${PG_DATABASE}?schema=public`
  execSync(`npx prisma migrate dev --name local_init --url "${dbUrl}"`, {
    cwd: rootDir,
    env: {
      ...process.env,
      DATABASE_URL: dbUrl,
    },
    stdio: 'inherit',
  })
  console.log('✅ Prisma migrations applied')
} catch (e) {
  console.error('❌ Prisma migration failed:', e.message)
  await pg.stop()
  process.exit(1)
}

// ── Start Anvil (fork Base mainnet) ───────────────────────────────────────────
console.log(`\n⛓️  Starting Anvil fork of Base mainnet on port ${ANVIL_PORT}...`)
console.log(`   Forking: ${BASE_MAINNET_RPC}`)

const anvilBin = process.env.HOME + '/.foundry/bin/anvil'
const anvil = spawn(anvilBin, [
  '--fork-url', BASE_MAINNET_RPC,
  '--port', String(ANVIL_PORT),
  '--chain-id', '8453',   // keep Base chain ID so viem's base chain config matches
  '--block-time', '2',    // mine a block every 2s (similar to Base)
  '--silent',
], { stdio: ['ignore', 'pipe', 'pipe'] })

anvil.stdout.on('data', d => process.stdout.write(`[anvil] ${d}`))
anvil.stderr.on('data', d => process.stderr.write(`[anvil] ${d}`))

// Give Anvil 3s to start
await new Promise(r => setTimeout(r, 3000))
console.log(`✅ Anvil forking Base mainnet on http://localhost:${ANVIL_PORT}`)

// ── Print summary ─────────────────────────────────────────────────────────────
console.log(`
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
  LOCAL DEV ENVIRONMENT READY
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
  PostgreSQL : localhost:${PG_PORT}  (db: ${PG_DATABASE})
  Anvil RPC  : http://localhost:${ANVIL_PORT}  (fork of Base mainnet)
  Contract   : ${CHECKIN_CONTRACT}

  ➡  Open a new terminal and run:
       npm run start:dev

  ➡  Then run the checkin test:
       node scripts/test-checkin.mjs

  Press Ctrl+C here to stop Postgres + Anvil.
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
`)

// ── Graceful shutdown ─────────────────────────────────────────────────────────
const shutdown = async (signal) => {
  console.log(`\n[${signal}] Shutting down...`)
  anvil.kill('SIGTERM')
  await pg.stop()
  console.log('✅ All services stopped.')
  process.exit(0)
}

process.on('SIGINT', () => shutdown('SIGINT'))
process.on('SIGTERM', () => shutdown('SIGTERM'))

// Keep process alive
setInterval(() => {}, 10_000)
